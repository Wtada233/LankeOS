/**
 * 构建时从 GitHub 获取 lpkg README，生成中英文 docs/lpkg/index.md
 *
 * 用法：node scripts/fetch-lpkg-docs.mjs
 *
 * 环境变量：
 *   GITHUB_OWNER   — 默认为 Wtada233
 *   GITHUB_REPO    — 默认为 LankeOS
 */

const OWNER = process.env.GITHUB_OWNER || 'Wtada233'
const REPO = process.env.GITHUB_REPO || 'LankeOS'
const BRANCH = 'main'

const headers = { 'User-Agent': 'lankeos-website-builder' }
if (process.env.GITHUB_TOKEN) {
  headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`
}

const LOCALES = {
  zh: {
    output: 'docs/lpkg/index.md',
    readmePath: 'lpkg/README.md',
    title: 'LPKG',
    disclaimer: '> ⚡ 此页面由 `scripts/fetch-lpkg-docs.mjs` 在构建时自动生成，从 GitHub 仓库的 `lpkg/README.md` 拉取。',
    heading: 'lpkg 包管理器',
  },
  en: {
    output: 'docs/en/lpkg/index.md',
    title: 'LPKG',
    readmePath: 'lpkg/README.en.md',
    disclaimer: '> ⚡ This page is auto-generated by `scripts/fetch-lpkg-docs.mjs` at build time from the `lpkg/README.en.md` in the GitHub repository.',
    heading: 'lpkg Package Manager',
  },
}

async function fetchRaw(path) {
  const url = `https://raw.githubusercontent.com/${OWNER}/${REPO}/${BRANCH}/${path}`
  const res = await fetch(url, { headers })
  if (!res.ok) throw new Error(`fetch ${path} failed: ${res.status}`)
  return res.text()
}

function githubMdToVitepress(md) {
  // 移除首行语言切换链接（如 "English | [中文](README.md)" 或 "[English](README.en.md) | 中文"）
  md = md.replace(/^[^\n]*\[.*?\]\(README(?:\.en)?\.md\)[^\n]*\n/, '')

  md = md
    .replace(/^#### /gm, '##### ')
    .replace(/^### /gm, '#### ')
    .replace(/^## /gm, '### ')
    .replace(/^# /gm, '## ')

  // 将相对路径（./xxx）转为 GitHub blob 链接
  md = md.replace(
    /\((\.[^)]*)\)/g,
    `(https://github.com/${OWNER}/${REPO}/blob/${BRANCH}/lpkg/$1)`
  )

  // 将裸 README.md / README.en.md 引用转为 GitHub blob 链接
  md = md.replace(
    /\(README(\.en)?\.md\)/g,
    (_, isEn) => `(https://github.com/${OWNER}/${REPO}/blob/${BRANCH}/lpkg/README${isEn || ''}.md)`
  )

  // 将图片相对路径转为 raw 链接
  md = md.replace(
    /\]\(([^)]+\.png)\)/g,
    `](https://raw.githubusercontent.com/${OWNER}/${REPO}/${BRANCH}/lpkg/$1)`
  )

  return md
}

async function main() {
  console.log(`Fetching lpkg docs from ${OWNER}/${REPO}...`)
  const fs = await import('fs')

  for (const localeKey of ['zh', 'en']) {
    const locale = LOCALES[localeKey]
    const readme = await fetchRaw(locale.readmePath)
    console.log(`  → ${locale.readmePath} fetched (${readme.length} chars)`)

    const body = githubMdToVitepress(readme)
    const frontmatter = `---
title: ${locale.title}
editLink: false
---

# ${locale.heading}

${locale.disclaimer}
`
    const content = frontmatter + '\n' + body + '\n'
    fs.writeFileSync(locale.output, content)
    console.log(`  → 已写入 ${locale.output} (${((frontmatter.length + body.length) / 1024).toFixed(1)} KB)`)
  }
}

main().catch(err => {
  console.error('fetch-lpkg-docs.mjs 失败:', err.message)
  process.exit(1)
})
